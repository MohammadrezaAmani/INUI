class TailwindCSS(BaseVoidElement):

    def __init__(
        self,
        *data: Any,
        url: str = TAILWINDCSS_URL,
        config: dict = TAILWINDCSS_CONFIG,
        customcss: str = TAILWINDCSS_CUSTOM_CSS,
        **attributes: Any
    ) -> None:
        self.url = url
        self.config = config
        self.customcss = customcss
        super().__init__(*data, **attributes)

    def render_to_string(self, prettify: bool = False):
        return (
            Script(src=self.url).render_to_string()
            + (
                Script(f"tailwind.config={json.dumps(self.config)}").render_to_string()
                if self.config
                else ""
            )
            + (
                Style(self.customcss, type_="text_tailwindcss").render_to_string()
                if self.customcss
                else ""
            )
        )


class BaseTW(BaseVoidElement):
    tag: Optional[str] = None

    def __init__(self, *values) -> None:
        self.values = values

    def render_to_string(self, prettify: bool = False) -> str:
        values = ""
        if self.values:
            values += " -"
            values += "-".join(list(map(str, self.values)))
        return self.tag or tw_class_to_lower(self.__class__.__name__) + values


